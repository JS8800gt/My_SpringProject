CREATE SEQUENCE SEQ_MEMBER;

CREATE TABLE TBL_MEMBER(
	MEMBER_NUMBER NUMBER CONSTRAINT PK_MEMBER PRIMARY KEY,
	MEMBER_ID VARCHAR2(50) UNIQUE,
	MEMBER_PW VARCHAR2(50),
	MEMBER_NAME VARCHAR2(50),
	MEMBER_EMAIL VARCHAR2(50) UNIQUE,
	MEMBER_PHONE_NUMBER VARCHAR2(50),
	MEMBER_GENDER VARCHAR2(50),
	MEMBER_SIGNUP_DATE DATE DEFAULT SYSDATE,
	MEMBER_TYPE VARCHAR2(50),
	MEMBER_STATUS VARCHAR2(50) DEFAULT '활동'
);

